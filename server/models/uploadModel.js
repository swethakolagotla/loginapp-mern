 import mongoose from "mongoose";

const Schema = mongoose.Schema;

const uploadSchema = new Schema({
 
    title: {
      type: String,
      required: true,
      trim: true
    },
    description: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Upload = mongoose.model("upload", uploadSchema,"uploads");

export default Upload;
